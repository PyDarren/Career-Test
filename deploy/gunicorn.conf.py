"""画己职测 — Gunicorn 生产环境配置。

品牌：画己职测
项目：career_test

说明：
    - bind: 绑定到 127.0.0.1:8000，由 Nginx 反向代理
    - workers: 基于 CPU 核数 2*核数+1，默认 4（2 核服务器）
    - worker_class: 优先使用 uvicorn.workers.UvicornWorker（ASGI），
      若未安装 uvicorn 则回退到 sync（WSGI）
    - preload_app: 预加载应用，减少内存占用、加快启动
    - max_requests: 防止内存泄漏，每 worker 处理 1000 请求后重启
    - 日志输出到 stdout/stderr，便于容器/Supervisor 采集

启动方式：
    gunicorn career_test.wsgi:application \
        --config deploy/gunicorn.conf.py \
        --env DJANGO_SETTINGS_MODULE=career_test.settings.prod
"""

import multiprocessing
import os

# ---------------------------------------------------------------------------
# 绑定地址（仅监听本地，由 Nginx 反向代理）
# ---------------------------------------------------------------------------
bind: str = "127.0.0.1:8000"

# ---------------------------------------------------------------------------
# Worker 数量：2 * CPU 核数 + 1
# ---------------------------------------------------------------------------
workers: int = int(os.environ.get("GUNICORN_WORKERS", 2 * multiprocessing.cpu_count() + 1))

# ---------------------------------------------------------------------------
# Worker 类型：优先 uvicorn（ASGI / WebSocket），不可用则回退 sync
# ---------------------------------------------------------------------------
try:
    import uvicorn.workers  # noqa: F401

    worker_class: str = "uvicorn.workers.UvicornWorker"
except ImportError:
    worker_class = "sync"

# ---------------------------------------------------------------------------
# 超时与保活
# ---------------------------------------------------------------------------
timeout: int = 120
keepalive: int = 5
graceful_timeout: int = 30

# ---------------------------------------------------------------------------
# Worker 重启策略（防止内存泄漏）
# ---------------------------------------------------------------------------
max_requests: int = 1000
max_requests_jitter: int = 50

# ---------------------------------------------------------------------------
# 预加载应用（减少内存占用、加快 worker 启动）
# ---------------------------------------------------------------------------
preload_app: bool = True

# ---------------------------------------------------------------------------
# 日志
# ---------------------------------------------------------------------------
loglevel: str = "info"
accesslog: str = "-"  # 输出到 stdout
errorlog: str = "-"  # 输出到 stderr

# ---------------------------------------------------------------------------
# 进程名
# ---------------------------------------------------------------------------
proc_name: str = "career_test"

# ---------------------------------------------------------------------------
# Django 配置模块
# ---------------------------------------------------------------------------
raw_env: list[str] = [
    "DJANGO_SETTINGS_MODULE=career_test.settings.prod",
]
